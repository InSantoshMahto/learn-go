package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
	"strings"
)

func main() {
	reader := bufio.NewReader(os.Stdin)
	fmt.Print("Enter age: ")
	ageString, _ := reader.ReadString('\n')
	ageStringTrim := strings.TrimSpace(ageString)
	age, _ := strconv.ParseFloat(ageStringTrim, 8)
	fmt.Println(age)
}
